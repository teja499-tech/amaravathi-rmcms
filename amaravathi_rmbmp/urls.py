from django.contrib import admin
from django.urls import path, include
from django.conf import settings
from django.conf.urls.static import static
from django.contrib.auth import views as auth_views
from django.views.generic import TemplateView
from apps.core.views import dashboard_view, permission_denied_view

urlpatterns = [
    path('admin/', admin.site.urls),
    path('', include('apps.core.urls')),
    path('users/', include('apps.users.urls')),
    path('suppliers/', include('apps.suppliers.urls')),
    path('customers/', include('apps.customers.urls')),
    path('expenses/', include('apps.expenses.urls')),
    path('accounts/', include('apps.accounts.urls')),
    path('materials/', include('apps.materials.urls')),
    path('employees/', include('employees.urls')),
    path('commitments/', include('apps.commitments.urls')),
    
    # Django auth views with explicit template paths
    path('login/', auth_views.LoginView.as_view(template_name='registration/login.html'), name='login'),
    path('logout/', auth_views.LogoutView.as_view(next_page='login'), name='logout'),
    path('password_reset/', auth_views.PasswordResetView.as_view(template_name='registration/password_reset_form.html'), name='password_reset'),
    path('password_reset/done/', auth_views.PasswordResetDoneView.as_view(template_name='registration/password_reset_done.html'), name='password_reset_done'),
    path('reset/<uidb64>/<token>/', auth_views.PasswordResetConfirmView.as_view(template_name='registration/password_reset_confirm.html'), name='password_reset_confirm'),
    path('reset/done/', auth_views.PasswordResetCompleteView.as_view(template_name='registration/password_reset_complete.html'), name='password_reset_complete'),
    
    # Dashboard view after login
    path('dashboard/', dashboard_view, name='dashboard'),
    
    # Permission denied page
    path('permission-denied/', permission_denied_view, name='permission_denied'),
]

# Add static and media URLs for development
if settings.DEBUG:
    urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)
    urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
